function hashStr(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return h >>> 0;
}

export interface DomainAssignment {
  primary: PrimaryDomain;
  secondary: SecondaryDomain;
}

export function deriveDomain(c: Company): DomainAssignment {
  const seed = hashStr(c.id);
  const r1 = (seed % 1000) / 1000;

  let primary: PrimaryDomain;
  const biases = c.techField ? TECHFIELD_DOMAIN_BIAS[c.techField] : undefined;
  if (biases && biases.length > 0 && r1 < 0.7) {
    primary = biases[seed % biases.length];
  } else {
    primary = PRIMARY_DOMAINS[seed % PRIMARY_DOMAINS.length];
  }

  const secondaries = DOMAIN_SECONDARIES[primary];
  const secondary = secondaries[(seed >>> 7) % secondaries.length];

  return { primary, secondary };
}
